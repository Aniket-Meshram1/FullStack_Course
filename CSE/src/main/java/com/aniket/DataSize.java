
package com.aniket;

public class DataSize {
	public static void main (String [] args) {
		int a = 45;
		System.out.println(a);
		int b = 045; //Octadecimal Number due to prefix 0
		System.out.println(b);
		int c = 0x45; // Hexadecimal number due to 0x 
		System.out.println(c);
		int d = 0b1011; // Binary number due to 0b 
		System.out.println(d);
		
		

	}
	

}
